    <h4>Common hazards:</h4>
    Every farm is different, but hazards common to most farms include:
    <br>
    <ul>
        <li><b>animals</b> – injuries inflicted by animals can include bites, kicks, crushing, ramming, trampling,
            and transmission of certain infectious diseases such as giardia, salmonella, ringworm and leptospirosis.
        </li><br>
        <li><b>chemicals</b> – pesticides and herbicides can cause injuries such as burns, respiratory illness or
            poisoning.</li><br>
        <li><b>confined spaces</b> – such as silos, water tanks, milk vats and manure pits
            may contain unsafe atmospheres, which can cause poisoning or suffocation.</li><br>
        <li><b>electricity</b> – dangers include faulty switches, cords, machinery or overhead power lines.</li><br>
        <li><b>heights</b> – falls from ladders, rooftops, silos and windmills are a major cause of injury.</li><br>
        <li><b>machinery</b> – hazards include tractors without roll-over protection structures (ROPS), power
            take-off (PTO) shafts, chainsaws, augers, motorbikes and machinery with unguarded moving parts.</li><br>
        <li><b>noise pollution</b> – noise from livestock, machinery and guns can affect your hearing.</li><br>
        <li><b>vehicles</b> – crashes or falls from motorbikes, two-wheel and quad bikes,
            tractors, utes and horses can result in major injuries.</li><br>
        <li><b>water</b> – drowning can occur in as little as five centimetres of water. Dams, lakes, ponds, rivers,
            channels, tanks, drums and creeks are all hazards. Young children are particularly at risk.</li><br>
        <li><b>weather</b> – hazards include sunburn, heat stroke, dehydration and hypothermia.
        </li>
    </ul>
    <br>



    <h4>Making your farm a safer workplace:</h4>
    Suggestions for making your farm a safer place to work include: <br><br>
    <ul>
        <li>Regularly walk around your farm and assess potential dangers.</li> <br>
        <li>Consult with farm safety advisers from the Victorian WorkCover Authority – they may provide free
            consultations.</li> <br>
        <li>Create a safe and contained play area for young children close to the house and away from hazards.</li>
        <br>
        <li>Make sure everyone working on the farm is properly educated on farm risks and trained in first aid.</li>
        <br>
        <li>Keep all equipment in good repair.</li> <br>
        <li>Store dangerous items such as machinery, firearms and chemicals behind locked doors and remove keys to a
            safe place.</li> <br>
        <li>Find ways to improve safety, such as fitting roll-over protection (ROPS) and seatbelts to tractors,
            or replacing dangerous chemicals with less toxic varieties.</li> <br>
        <li>Keep a log of injuries and near-misses to pinpoint areas for improvement.</li> <br>
        <li>Consult with other workers and family members on how to improve safety.</li> <br>
        <li>Write a safety plan together that includes ways to identify hazards and minimise potential risks.</li>
        <br>
        <li>Always use appropriate safety equipment, such as machinery guards and shields, helmets, gloves, goggles
            or breathing apparatus.</li> <br>
        <li>Make sure everyone understands and uses safety procedures, especially children.</li> <br>
        <li>If using four-wheel motorbikes, make sure you are using them in line with the recommendations
            – remember they are not all-terrain vehicles. </li> <br>
    </ul>

