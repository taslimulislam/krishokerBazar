    <h4>Soil conservation:</h4>
    Soil conservation is the prevention of soil loss from erosion or prevention
    of reduced fertility caused by over usage, acidification, salinization or other chemical soil contamination.

    Slash-and-burn and other unsustainable methods of subsistence farming are practiced in some lesser developed
    areas.
    A sequel to the deforestation is typically large scale erosion, loss of soil nutrients and sometimes
    total desertification. Techniques for improved soil conservation include crop rotation, cover crops,
    conservation tillage and planted windbreaks, affect both erosion and fertility. When plants die,
    they decay and become part of the soil. Code 330 defines standard methods recommended by the U.S.
    Natural Resources Conservation Service. Farmers have practiced soil conservation for millennia.
    In Europe, policies such as the Common Agricultural Policy are targeting the application of best
