    <b>Contour ploughing:</b>
    <br>
    Contour ploughing orients furrows following the contour lines of the farmed area. Furrows move left and right
    to maintain a constant altitude, which reduces runoff. Contour ploughing was practiced by the ancient
    Phoenicians,
    and is effective for slopes between two and ten percent.[3] Contour ploughing can increase crop yields
    from 10 to 50 percent, partially as a result of greater soil retention.

    <br><br>
    <b>Terrace farming:</b>
    <br>
    Terracing is the practice of creating nearly level areas in a hillside area. The terraces form a series of
    steps,
    each at a higher level than the previous. Terraces are protected from erosion by other soil barriers.
    Terraced farming is more common on small farms and in underdeveloped countries, since mechanized
    equipment is difficult to deploy in this setting.
    <br><br>

    <b>Keyline design:</b>
    <br>
    Keyline design is an enhancement of contour farming, where the total watershed
    properties are taken into account in forming the contour lines.

    <br><br>
    <b>Perimeter runoff control:</b>
    <br>
    Tree, shrubs and ground-cover are effective perimeter treatment for soil erosion prevention,
    by impeding surface flows. A special form of this perimeter or inter-row treatment is the
    use of a "grass way" that both channels and dissipates runoff through surface friction,
    impeding surface runoff and encouraging infiltration of the slowed surface water.

    <br><br>
    <b>Windbreaks:</b>
    <br>
    Windbreaks are sufficiently dense rows of trees at the windward exposure of an agricultural field subject
    to wind erosion.[6] Evergreen species provide year-round protection; however, as long as foliage is present
    in the seasons of bare soil surfaces, the effect of deciduous trees may be adequate.

    <br><br>
    <b>Cover crops/crop rotation:</b>
    <br>
    Cover crops such as legumes plant, white turnip, radishes and other species are rotated with cash
    crops to blanket the soil year-round and act as green manure that replenishes nitrogen and other
    critical nutrients. Cover crops also help suppress weeds.

    <br><br>
    <b>Soil-conservation farming:</b>
    <br>
    Soil-conservation farming involves no-till farming, "green manures" and other soil-enhancing practices.
    Such farming methods attempt to mimic the biology of barren lands. They can revive damaged soil, minimize
    erosion,
    encourage plant growth, eliminate the use of nitrogen fertilizer or fungicide, produce above-average yields
    and protect crops during droughts or flooding. The result is less labor and lower costs that increase farmers’
    profits. No-till farming and cover crops act as sinks for nitrogen and other nutrients. This increases the
    amount of soil organic matter.[7]

    Repeated plowing/tilling degrades soil, killing its beneficial fungi and earthworms. Once damaged, soil may take
    multiple
    seasons to fully recover, even in optimal circumstances.[7]

    Critics argue that no-till and related methods are impractical and too expensive for many growers,
    partly because it requires new equipment. They cite advantages for conventional tilling depending on the
    geography,
    crops and soil conditions. Some farmers claimed that no-till complicates weed control, delays planting and
    that post-harvest residues, especially for corn, are hard to manage.[7]

