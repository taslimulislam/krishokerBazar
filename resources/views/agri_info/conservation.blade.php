    <b>Salinity management:</b>
    <br>
    Salinity in soil is caused by irrigating with salty water. Water then evaporates from the soil leaving the
    salt behind. Salt breaks down the soil structure, causing infertility and reduced growth.

    The ions responsible for salination are: sodium (Na+), potassium (K+), calcium (Ca2+), magnesium (Mg2+)
    and chlorine (Cl-). Salinity is estimated to affect about one third of the earth’s arable land.[8] Soil
    salinity adversely affects crop metabolism and erosion usually follows.

    Salinity occurs on drylands from overirrigation and in areas with shallow saline water tables.
    Over-irrigation deposits salts in upper soil layers as a byproduct of soil infiltration; irrigation merely
    increases the rate of salt deposition. The best-known case of shallow saline water table capillary
    action occurred in Egypt after the 1970 construction of the Aswan Dam. The change in the groundwater
    level led to high salt concentrations in the water table. The continuous high level of the water
    table led to soil salination.

    Use of humic acids may prevent excess salination, especially given excessive irrigation.[citation needed] Humic
    acids can fix both anions and cations and eliminate them from root zones.

    <br><br>
    <b>Soil organisms:</b>
    <br>
    When worms excrete feces in the form of casts, a balanced selection of minerals and plant nutrients is made
    into a form accessible for root uptake. Earthworm casts are five times richer in available nitrogen,
    seven times richer in available phosphates and eleven times richer in available potash than the
    surrounding upper 150 millimetres (5.9 in) of soil. The weight of casts produced may be greater
    than 4.5 kg per worm per year. By burrowing, the earthworm improves soil porosity, creating channels
    that enhance the processes of aeration and drainage.[9]

    Other important soil organisms include nematodes, mycorrhiza and bacteria.

    Degraded soil requires synthetic fertilizer to produce high yields. Lacking structure increases erosion
    and carries nitrogen and other pollutants into rivers and streams.[7]

    Each one percent increase in soil organic matter helps soil hold 20,000 gallons more water per acre.

    <br><br>
    <b>Mineralization:</b>
    <br>
    To allow plants full realization of their phytonutrient potential, active mineralization of the soil is
    sometimes undertaken. This can involve adding crushed rock or chemical soil supplements. In either case the
    purpose
    is to combat mineral depletion. A broad range of minerals can be used, including common substances such as
    phosphorus
    and more exotic substances such as zinc and selenium. Extensive research examines the phase transitions
    of minerals in soil with aqueous contact.[10]

    Flooding can bring significant sediments to an alluvial plain. While this effect may not be desirable
    if floods endanger life or if the sediment originates from productive land, this process of addition to a
    floodplain is a natural process that can rejuvenate soil chemistry through mineralization.

